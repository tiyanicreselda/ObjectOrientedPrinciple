using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Objectorientatedprinciples
{
    class People
    {
        public string Name;
        public int Gender;
        public string Race;
        public string Age;


        public People(string PersonName, string PersonGender, string PersonRace, int PersonAge)
        {
            Name = PersonName;
            Gender = PersonAge;
            Race = PersonRace;
            Age = PersonGender;
        }
    }
}